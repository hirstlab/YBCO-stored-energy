%run chaplot_murphy_0K and averaging_temperature_sims before running this

% mDPA values corresponding to the avg_mdpa arrays
mdpa_values = [0, 0.5, 1, 2, 4, 8]; % Include 0 explicitly

% Temperatures corresponding to the indices
temperatures = [1, 20, 92, 300];

% Combine the avg_mdpa arrays into a matrix
avg_energy = [avg_mdpa0; avg_mdpa05; avg_mdpa1; avg_mdpa2; avg_mdpa4; avg_mdpa8] - avg_mdpa0; % 6 x 9 matrix

% Viridis colormap for distinct temperature lines
colors = viridis(length(temperatures) + 2); % Generate colormap

% Figure setup
figure;
hold on;

indexfix = [1, 3, 4, 5, 9]; % Indices for selected temperatures

plot(x_murphy_filtered, y_murphy_filtered, 'x', 'MarkerSize', 8, 'LineWidth', 1.5, ...
    'Color', colors(1, :), 'DisplayName', 'Murphy Avg');
hold on
% Power law fit for Murphy
plot(x_curve, y_curve_murphy, '-', 'LineWidth', 1.5, 'Color', colors(1, :), ...
    'DisplayName', sprintf('Murphy Fit: y = %.3f * x^{%.3f}', a_murphy, b_murphy));
hold on
% Loop over each temperature and perform power law fit constrained to (0,0)
for i = 1:length(temperatures)
    y_data = avg_energy(:, indexfix(i)) * conversion_factor; % Include 0 mDPA
    
    % Define the power law function constrained to (0,0): y = a * x^b
    power_law = @(p, x) p(1) * x.^p(2);

    % Initial guess for parameters [a, b]
    p0 = [1, 1];

    % Exclude x = 0 from fitting but ensure y(0) = 0 in the final curve
    x_fit_data = mdpa_values(2:end)'; % Exclude 0
    y_fit_data = y_data(2:end); % Exclude 0

    % Perform nonlinear least squares fitting
    opts = optimset('Display', 'off');
    p_fit = lsqcurvefit(power_law, p0, x_fit_data, y_fit_data, [], [], opts);
    a = p_fit(1);
    b = p_fit(2);

    % Generate fitted values including x=0
    x_fit = linspace(0, 8, 100)'; % Ensure column vector
    y_fit = a * x_fit.^b;
    y_fit(1) = 0; % Explicitly enforce y(0) = 0

    % Compute R² value
    y_pred = a * x_fit_data.^b;
    ss_res = sum((y_fit_data - y_pred).^2);
    ss_tot = sum((y_fit_data - mean(y_fit_data)).^2);
    r2 = 1 - (ss_res / ss_tot);

    % Print power law equation and R² value
    fprintf('%d K: y = %.3f * x^{%.3f}, R² = %.4f\n', temperatures(i), a, b, r2);

    % Plot the experimental data points
    plot(mdpa_values, y_data, 'x', 'LineWidth', 1.5, 'Color', colors(i+1, :), ...
        'DisplayName', sprintf('%d K (Data)', temperatures(i)));

    % Plot the power law fit constrained through (0,0)
    plot(x_fit, y_fit, '-', 'LineWidth', 1.5, 'Color', colors(i+1, :), ...
        'DisplayName', sprintf('%d K Fit: y = %.2f * x^{%.2f}', a, b));
end

% Labels, legend, and formatting
set(gca, 'FontSize', 14, 'FontName', 'Helvetica');
xlabel('mDPA');
ylabel('Stored Energy (J/cm^3)');
